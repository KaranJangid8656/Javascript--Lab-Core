// Adding element in an array at the end using push()

let a =  ["HTML","CSS","JS"]
a.push("Node.js");

// Adding an element at beginning of the array using unshift()

a.unshift("React");
console.log(a)

// Removing elements from array 
// To remove elements from array we have many methods

// Removing an element at beginning of the array using shift()
// It removes and returns the first element
let b = [ 1,2,3,4,5]
b.shift();
console.log(b)

// Remove last element using pop()
let c = [ 1,2,3,4,5]
c.pop()
console.log(c)








